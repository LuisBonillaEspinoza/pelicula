    private void jtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtableMouseClicked
        // TODO add your handling code here:
        int row = jtable.getSelectedRow();
        if(row<-1){
            JOptionPane.showMessageDialog(null,"Seleccione una fila correcta");
        }
        else{
            txtdni.setText(jtable.getValueAt(row,0).toString());
            txtname.setText(jtable.getValueAt(row,1).toString());
            txtape.setText(jtable.getValueAt(row,2).toString());
            txtrol.setText(jtable.getValueAt(row,3).toString());
        }
    }//GEN-LAST:event_jtableMouseClicked

