    void tabla(){
        String titulos[] = {"Dni","Nombre","Apellido","Rol"};
        DefaultTableModel tablita = new DefaultTableModel(null,titulos);
        String datos[] = new String[4];
        Connection con = new DBManager().obtenerConexion();
        ResultSet rs;
        try {
            PreparedStatement pst = con.prepareStatement("select usuario.usuario_txt_dni,usuario.usuario_txt_nom,usuario.usuario_txt_ape,rol.rol_txt_descrip from usuario inner join rol on usuario.rol_num_cod=rol.rol_num_cod");
            rs = pst.executeQuery();
            while(rs.next()){
                datos[0] = rs.getString("usuario_txt_dni");
                datos[1] = rs.getString("usuario_txt_nom");
                datos[2] = rs.getString("usuario_txt_ape");
                datos[3] = rs.getString("rol_txt_descrip");
                tablita.addRow(datos);
            }
        } catch (Exception e) {
            System.out.println("Error al mostrar datos de la tabla : " + e.getMessage());
        }
        jtable.setModel(tablita);
    }
