    void genero(){
        Negocios.Genero gen = new Negocios.Genero();
        ArrayList<clsGenero> ge = gen.MostrarGenero();
        Iterator ite = ge.iterator();
        String datos[] = new String[10000];
        while(ite.hasNext()){
            int i = 0;
            clsGenero g = new clsGenero();
            g = (clsGenero) ite.next();
            datos[i] = g.getGenero_txt_descrip();
            jgen.addItem(datos[i]);
        }
    }
