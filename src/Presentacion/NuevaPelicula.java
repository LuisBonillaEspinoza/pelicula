    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = txtnombre.getText();
        String sipno = txtsipno.getText();
        String exi = txtex.getText();
        String pre = txtpre.getText();
        String gen = String.valueOf(jgen.getSelectedIndex() +1);
        
        if(nombre.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese un Nombre correcto");
        }
        else if(sipno.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese una sipnosis correcta");
        }
        else if(exi.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese una cantidad correcta");
        }
        else if(pre.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese un precio correccto");
        }
        else{
            Pelicula peli = new Pelicula();
            clsPelicula pe = new clsPelicula();
            pe.setPelicula_txt_nom(nombre);
            pe.setPelicula_txt_sipnosis(sipno);
            pe.setPelicula_num_existencia(exi);
            pe.setPelicula_double_precio(pre);
            pe.setGenero_num_cod(gen);
            peli.IngresarPelicula(pe);
            JOptionPane.showMessageDialog(null,"Ingresado Correctamente");
            MenuAdministrador men = new MenuAdministrador();
            this.dispose();
            men.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

