    private void jtablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtablaMouseClicked
        // TODO add your handling code here:
        int row = jtabla.getSelectedRow();
        boolean estado=true;
        if(row<-1){
            JOptionPane.showMessageDialog(null,"Seleccione una fila correcta");
        }
        else{
            txtcod.setText(jtabla.getValueAt(row,0).toString());
            txtsal.setText(jtabla.getValueAt(row,1).toString());
            txtpass.setText(jtabla.getValueAt(row,2).toString());
            jRadioButton1.setSelected(estado);
        }
    }//GEN-LAST:event_jtablaMouseClicked

