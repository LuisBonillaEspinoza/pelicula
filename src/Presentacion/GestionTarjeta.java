    void tabla(){
        String titulos[] = {"Codigo","Saldo","Contraseña","Propietario"};
        DefaultTableModel tablita = new DefaultTableModel(null,titulos);
        String datos[] = new String[5];
        try {
            Connection con = new DBManager().obtenerConexion();
            ResultSet rs;
            PreparedStatement pst = con.prepareStatement("select tarjeta.tarjeta_num_cod,tarjeta.tarjeta_double_saldo,tarjeta.tarjeta_txt_pass,usuario.usuario_txt_nom from tarjeta inner join usuario on tarjeta.usuario_txt_dni=usuario.usuario_txt_dni");
            rs = pst.executeQuery();
            while(rs.next()){
                datos[0] = rs.getString("tarjeta_num_cod");
                datos[1] = rs.getString("tarjeta_double_saldo");
                datos[2] = rs.getString("tarjeta_txt_pass");
                datos[3] = rs.getString("usuario_txt_nom");
                tablita.addRow(datos);
            }
        } catch (Exception e) {
            System.out.println("Error al mostrar datos : " + e.getMessage());
        }
        jtabla.setModel(tablita);
    }
