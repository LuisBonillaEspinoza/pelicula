    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String cod = txtcod.getText();
        String saldo = txtsal.getText();
        String pass = txtpass.getText();
        String dni = String.valueOf(jdrni.getSelectedItem());
        if(cod.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese un Codigo Correcto");
        }
        else if(saldo.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese un Saldo Correcto");
        }
        else if(pass.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese una Contraseña Correcta");
        }
        else{
            clsTarjeta tar = new clsTarjeta();
            tar.setTarjeta_num_cod(cod);
            tar.setTarjeta_double_saldo(saldo);
            tar.setTarjeta_txt_pass(pass);
            tar.setUsuario_txt_dni(dni);
            Tarjeta ta = new Tarjeta();
            ta.IngresoTarjeta(tar);
            JOptionPane.showMessageDialog(null,"Ingresado Correctamente");
            MenuAdministrador men = new MenuAdministrador();
            this.dispose();
            men.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

