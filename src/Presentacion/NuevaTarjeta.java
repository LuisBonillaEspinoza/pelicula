    void dni(){
        Negocios.Usuario us = new Negocios.Usuario();
        ArrayList<clsUsuario> u = us.MostrarUsuario();
        Iterator it = u.iterator();
        String datos[] = new String[1000];
        while(it.hasNext()){
            int i = 0;
            clsUsuario usu = new clsUsuario();
            usu = (clsUsuario) it.next();
            datos[i] = usu.getUsuario_txt_dni();
            jdrni.addItem(datos[i]);
        }
    }
