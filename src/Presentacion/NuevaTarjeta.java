    private void txtcodKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcodKeyTyped
        // TODO add your handling code here:
                char caracter = evt.getKeyChar();
        if (txtcod.getText().length() > 15) {
            evt.consume();

            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Se llego al limite de caracteres");
        }
        if(Character.isLetter(caracter)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingresar Solo Numeros");
        }
    }//GEN-LAST:event_txtcodKeyTyped

    private void txtsalKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsalKeyTyped
        // TODO add your handling code here:
                if(!Character.isDigit(evt.getKeyChar())&& evt.getKeyChar()!='.'){
            evt.consume();
        }
        if(evt.getKeyChar()=='.'&&txtsal.getText().contains(".")){
            evt.consume();
        }
    }//GEN-LAST:event_txtsalKeyTyped

